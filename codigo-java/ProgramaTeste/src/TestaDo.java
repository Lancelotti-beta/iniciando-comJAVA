
public class TestaDo {

	public static void main(String[] args) {
		int number = 1;
		int cont = 0;
		
		do {
			System.out.println("Esse é um teste.");
			System.out.println();
			System.out.println("Agora faz até que sentido. só falta entender a finalidade");
			cont++;
		} while(number >= cont);
	}
}
